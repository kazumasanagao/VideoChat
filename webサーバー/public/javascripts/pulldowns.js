function changeRegion() {
    var region_pull = document.getElementById("region");
    var country_pull = document.getElementById("country");
    var region = region_pull[region_pull.selectedIndex].value;
    var firstRow = '<option value="">Country</option> +';
    switch (region) {
        case "":
            country_pull.innerHTML = firstRow + countriesHTML;
            break;
        case "as":
            country_pull.innerHTML = firstRow + asiaHTML;
            break;
        case "af":
            country_pull.innerHTML = firstRow + africaHTML;
            break;
        case "eu":
            country_pull.innerHTML = firstRow + europeHTML;
            break;
        case "lc":
            country_pull.innerHTML = firstRow + latinAmericaHTML;
            break;
        case "na":
            country_pull.innerHTML = firstRow + northAmericaHTML;
            break;
        case "oc":
            country_pull.innerHTML = firstRow + oceaniaHTML;
            break;
        case "me":
            country_pull.innerHTML = firstRow + middleEastHTML;
            break;
        default:
            country_pull.innerHTML = firstRow + countriesHTML;
            break;
    } 
}

